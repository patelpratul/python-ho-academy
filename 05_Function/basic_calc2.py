
def add(num1, num2):
    add = num1 + num2
    return add

def subtract(num1, num2):
    subtract = num1 - num2
    return subtract

def multiply(num1, num2):
    multiply = num1 * num2
    return multiply


print ("Addition:", add(20, 10))
print ("Subtraction:", subtract(20, 10))
print ("Multiplication:", multiply(20, 10))
