# Integer variables
num1 = 20
num2 = 10

# Basic Arithmetic Operations

result1 = num1 + num2
print("Addition:", result1)


result2 = num1 - num2
print("Subtraction:", result2)

result3 = num1 * num2
print("Multiplication:", result3)

#Integer division got extra - check float division
result4 = num1 // num2  
print("Division:", result4)


# Float variables
num3 = 5.0
num4 = 2.5

result5 = num3 + num4
print("Addition:", result5)


result6 = num3 - num4
print("Subtraction:", result6)

result7 = num3 * num4
print("Multiplication:", result7)

result8 = num3 / num4
print("Division:", result8)


# Rounding
result9 = round(4.55559265678, 2)  # Rounds to 2 decimal places
print("Rounded:", result9)

# Modulus (Remainder)
result10 = num1 % num2
print("Modulus (Remainder):", result10)

# Absolute Value
result11 = abs(-7)
print("Absolute Value:", result11)
