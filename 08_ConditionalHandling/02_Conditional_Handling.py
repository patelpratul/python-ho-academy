
# 1 If Example 
x = 10
if x > 5:
    print("x is greater than 5")

# 2 elif Example
# The `elif` statement is short for "else if" and is used to check multiple

x = 10
if x > 15:
    print("x is greater than 15")
elif x > 5:
    print("x is greater than 5 but not greater than 15")
else:
    print("x is not greater than 5")


## `else` Statement

# The `else` statement is used to specify a block of code to execute when none of the previous conditions (in the `if` and `elif` statements) are `True`.


y = 3
if y > 5:
    print("y is greater than 5")
else:
    print("y is not greater than 5")

